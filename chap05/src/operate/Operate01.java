package operate;

public class Operate01 {

	public static void main(String[] args) {
		int a = 10;
		int b = 10;
		int c = 10;
		int d = 10;
		
		System.out.println("a++ =>" + (a++));
		System.out.println("a =>" + + a);
		System.out.println("++b =>" + (++b));
		System.out.println("c-- =>" + (c--));
		System.out.println("--d =>" + (--d));
		
	}

}
